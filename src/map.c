static
void cp_x_y(game_handler_t *g_h, int i)
{
    for (int y = 0; y < MAP_HEIGHT && i == 0; y++)
        for (int x = 0; x < MAP_WIDTH; x++)
            g_h->game->level.map[i][y][x] = map1[y][x];
    for (int y = 0; y < MAP_HEIGHT && i == 1; y++)
        for (int x = 0; x < MAP_WIDTH; x++)
            g_h->game->level.map[i][y][x] = map2[y][x];
    for (int y = 0; y < MAP_HEIGHT && i == 2; y++)
        for (int x = 0; x < MAP_WIDTH; x++)
            g_h->game->level.map[i][y][x] = map3[y][x];
}

static
bool init_map(game_handler_t *g_h)
{
    for (int i = 0; i < 3; i++)
        cp_x_y(g_h, i);
    return true;
}

bool load_map(game_handler_t *g_h)
{
    if (g_h->game->have_been_f || !load_from_file(g_h, "actual_level.bin",
        &g_h->game->level.actual_level,
        sizeof(g_h->game->level.actual_level)))
        g_h->game->level.actual_level = 0;
    return init_map(g_h);
}
